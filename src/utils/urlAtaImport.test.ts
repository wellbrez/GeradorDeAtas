import LZString from 'lz-string'
import { afterEach, describe, expect, it } from 'vitest'
import type { MeetingMinutesStorage } from '@/types'
import { encodeAtaToHash, parseAtaFromHash } from './urlAtaImport'

const baseStorage: MeetingMinutesStorage = {
  cabecalho: {
    numero: 'ATA-001',
    data: '2026-03-07',
    tipo: 'Ordinaria',
    titulo: 'Reuniao semanal',
    responsavel: 'Maria Silva',
    projeto: 'Projeto Alpha',
  },
  attendance: [
    {
      nome: 'Maria Silva',
      email: 'maria@example.com',
      empresa: 'Vale',
      telefone: '31999990000',
      presenca: 'P',
    },
  ],
  itens: [
    {
      id: 'item-1',
      item: '1',
      nivel: 1,
      pai: null,
      filhos: [],
      criadoEm: '2026-03-07T10:00:00.000Z',
      historico: [
        {
          id: 'hist-1',
          criadoEm: '2026-03-07T10:00:00.000Z',
          descricao: '<b>Verificar risco</b>',
          responsavel: { nome: 'Maria Silva', email: 'maria@example.com' },
          data: '2026-03-10',
          status: 'Pendente',
        },
      ],
      UltimoHistorico: {
        id: 'hist-1',
        criadoEm: '2026-03-07T10:00:00.000Z',
        descricao: '<b>Verificar risco</b>',
        responsavel: { nome: 'Maria Silva', email: 'maria@example.com' },
        data: '2026-03-10',
        status: 'Pendente',
      },
    },
  ],
}

function utf8ToBase64(value: string): string {
  const bytes = new TextEncoder().encode(value)
  const binary = Array.from(bytes)
    .map((b) => String.fromCharCode(b))
    .join('')
  return btoa(binary)
}

describe('urlAtaImport', () => {
  afterEach(() => {
    window.location.hash = ''
  })

  it('parses compressed hash links generated by encodeAtaToHash', () => {
    window.location.hash = `#${encodeAtaToHash(baseStorage)}`

    expect(parseAtaFromHash()).toEqual(baseStorage)
  })

  it('parses legacy pure-base64 hash links', () => {
    const legacyHash = utf8ToBase64(JSON.stringify(baseStorage))
    window.location.hash = `#${legacyHash}`

    expect(parseAtaFromHash()).toEqual(baseStorage)
  })

  it('parses minimal QR payload format', () => {
    const minimalPayload = {
      c: baseStorage.cabecalho,
      a: baseStorage.attendance,
      i: baseStorage.itens.map((item) => ({
        id: item.id,
        item: item.item,
        nivel: item.nivel,
        pai: item.pai,
        filhos: item.filhos,
        criadoEm: item.criadoEm,
        u: item.UltimoHistorico,
      })),
    }
    const minimalHash = 'm' + LZString.compressToBase64(JSON.stringify(minimalPayload))
    window.location.hash = `#${minimalHash}`

    expect(parseAtaFromHash()).toEqual(baseStorage)
  })

  it('returns null for invalid hash payload', () => {
    window.location.hash = '#znot-valid'

    expect(parseAtaFromHash()).toBeNull()
  })
})
